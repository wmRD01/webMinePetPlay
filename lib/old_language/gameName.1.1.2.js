var companyIconCreate = function () {
  const libRootUrl = window.libraryPath && window.libraryPath != "" ? window.libraryPath.replace(
    /\/([^\/]+\/[^\/]+)$/, '') + '/lib' : '../../lib';
  let comName = window.companyIcon || 'og';
  comName = (comName != "") ? comName : 'og';
  createLogoImg();
  window.lang = (!window.lang || window.lang == "") ? "CNY" : window.lang;
  // 動態更換網址列圖示 標籤名稱
  const linkElem = document.createElement('link');
  linkElem.rel = 'shortcut icon';
  linkElem.href = libRootUrl + '/for1X/images/favicon_' + comName + '.ico';
  headElem.appendChild(linkElem);
  const titleElem = document.getElementsByTagName("title")[0];
  titleElem.innerHTML = gameName_Mode[window.lang][titleElem.innerHTML];

  function createLogoImg() {
    const logoUrl = libRootUrl + "/for1X/images/splash_" + comName + ".png";
    const splashElem = document.getElementById('splash');
    const logoWrap = document.getElementById('logo-wrap');
    const logoImgElem = document.createElement('img');
    logoWrap.appendChild(logoImgElem);
    logoImgElem.src = logoUrl;
    logoImgElem.onload = function () {
      splashElem.classList.add('active');
    };
  }
};

companyIconCreate();